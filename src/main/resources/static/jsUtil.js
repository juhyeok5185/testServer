/**
 * 엘리먼트 조회
 * @param target - id , class , name , tag 속성
 * @returns Element
 */
function find(target){
    return document.querySelector()
}


/**
 * 엘리먼트 리스트 조회
 * @param target - class , name , tag 속성
 * @returns Node List
 */
function findAll(target) {
    return document.querySelectorAll(target);
}


